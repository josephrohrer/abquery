			<h2>Executive Summary</h2>
				<p>Desktop and mobile website for home buyers and home owners in Albuquerque, New Mexico. Users are able to research both positive and negative attributes of the neighborhood they are living in/ potentially moving to. After inputting an address, information about proximity to public parks and/or various criminal activity will be displayed. Potentially, data returned will be displayed in an option box listing crimes or parks that can be selected (see wireframe for visuals), whatever box is selected can then be viewed in a map with a rating system.</p>
			<h2>User Stories</h2>
				<ol>
					<li>As a user, I want to know what the crime rate is in a neighborhood I am potentially buying a home.</li>
					<li>As a user, I want to know what the crime rates is in a neighborhood I am potentially buying a business.</li>
					<li>As a user, I want to know what types of crimes are committed in the neighborhood I currently live in.</li>
					<li>As a user, I want to know where parks are at in a neighborhood I am potentially buying a home.</li>
					<li>As a user, I want to know where parks are at in the neighborhood I currently live in.</li>
